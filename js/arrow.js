(function(document){
    var div = document.getElementById('btn');
    var icon = document.getElementById('icon');
    var open = false;
  
    div.addEventListener('click', function(){
      if(open){
        icon.className = 'fa fa-chevron-right';  
      } else{
        icon.className = 'fa fa-chevron-right open';
      }
  
      open = !open;
    });
  })(document);

